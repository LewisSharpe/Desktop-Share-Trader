    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        String companyname = txt_id.getText();
        String cardtype = txt_name.getText();
        String cardno = txt_contactNo.getText();
        String name = txt_address.getText();
        String expirydate = txt_email.getText();

        Card card = new Card(companyname, cardtype, cardno, name, expirydate);

        try {
            int addStudent = CardController.addStudent(card);
            if (addStudent > 0) {
                JOptionPane.showMessageDialog(rootPane, "Student Added OK !!!");
            }
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
        catch (SQLException e) {
            e.printStackTrace();
        }

        txt_id.setText("");
        txt_name.setText("");
        txt_contactNo.setText("");
        txt_address.setText("");
        txt_email.setText("");


    }//GEN-LAST:event_btn_addActionPerformed

